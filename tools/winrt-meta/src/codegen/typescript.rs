fn generate_factory_method_invoke(
    class: &ClassMeta,
    iface: &InterfaceMeta,
    method: &MethodMeta,
    known_types: &HashSet<String>,
) -> String {
    let in_params = get_in_params(method);
    let ts_params = ts_param_list(&in_params, known_types);

    let is_async = method.return_type.as_ref().is_some_and(|rt| rt.is_async());
    let return_ts_type = if is_async {
        format!("Promise<{}>", class.name)
    } else {
        class.name.clone()
    };

    let mut out = String::new();
    let async_kw = if is_async { "async " } else { "" };
    out.push_str(&format!(
        "    static {}{}({}): {} {{\n",
        async_kw, to_camel_case(&method.name), ts_params, return_ts_type
    ));

    // Get factory, cast to interface
    out.push_str(&format!(
        "        const factory = DynWinRtValue.activationFactory('{}')\n",
        class.full_name
    ));
    out.push_str(&format!(
        "            .cast(IID_{});\n",
        iface.name
    ));

    // Invoke
    let args_expr = build_args_expr(&in_params);
    let invoke_expr = format!(
        "{}.method({}).invoke(factory, [{}])",
        iface.name, method.vtable_index, args_expr
    );

    if is_async {
        out.push_str(&format!(
            "        return new {}(await {}.toPromise());\n",
            class.name, invoke_expr
        ));
    } else {
        out.push_str(&format!(
            "        return new {}({});\n",
            class.name, invoke_expr
        ));
    }
    out.push_str("    }\n");
    out
}

fn generate_static_method_invoke(
    class: &ClassMeta,
    iface: &InterfaceMeta,
    method: &MethodMeta,
    known_types: &HashSet<String>,
) -> String {
    let in_params = get_in_params(method);
    let ts_params = ts_param_list(&in_params, known_types);

    let return_type = method.return_type.as_ref();
    let is_async = return_type.is_some_and(|rt| rt.is_async());
    let ts_return = ts_return_type_safe(return_type, is_async, known_types);

    let mut out = String::new();

    // Static property getter
    if method.is_property_getter && in_params.is_empty() {
        let prop_name = to_camel_case(method.name.strip_prefix("get_").unwrap_or(&method.name));
        out.push_str(&format!("    static get {}(): {} {{\n", prop_name, ts_return));
        out.push_str(&format!(
            "        const statics = DynWinRtValue.activationFactory('{}')\n",
            class.full_name
        ));
        out.push_str(&format!(
            "            .cast(IID_{});\n",
            iface.name
        ));
        let invoke_expr = format!(
            "{}.method({}).invoke(statics, [])",
            iface.name, method.vtable_index
        );
        let converted = convert_return(&invoke_expr, return_type, false);
        out.push_str(&format!("        return {};\n", converted));
        out.push_str("    }\n");
    } else {
        let async_kw = if is_async { "async " } else { "" };
        out.push_str(&format!(
            "    static {}{}({}): {} {{\n",
            async_kw, to_camel_case(&method.name), ts_params, ts_return
        ));
        out.push_str(&format!(
            "        const statics = DynWinRtValue.activationFactory('{}')\n",
            class.full_name
        ));
        out.push_str(&format!(
            "            .cast(IID_{});\n",
            iface.name
        ));
        let args_expr = build_args_expr(&in_params);
        let invoke_expr = format!(
            "{}.method({}).invoke(statics, [{}])",
            iface.name, method.vtable_index, args_expr
        );
        let converted = convert_return(&invoke_expr, return_type, is_async);
        out.push_str(&format!("        return {};\n", converted));
        out.push_str("    }\n");
    }
    out
}

/// Generate an instance method for an interface wrapper class.
/// Uses `_InterfaceName` as the registered interface variable and `this._obj` directly.
fn generate_iface_instance_method(
    _iface: &InterfaceMeta,
    iface_var: &str,
    method: &MethodMeta,
    known_types: &HashSet<String>,
) -> String {
    // Interface wrapper: obj is already cast via from(), use this._obj directly
    generate_method_body(iface_var, "this._obj", method, known_types)
}

fn generate_instance_method_invoke(
    default_iface: &InterfaceMeta,
    method: &MethodMeta,
    known_types: &HashSet<String>,
) -> String {
    // RuntimeClass wrapper: need to cast to default interface each time
    let cast_expr = if !default_iface.iid.is_empty() {
        format!("this._obj.cast(IID_{})", default_iface.name)
    } else {
        "this._obj".to_string()
    };
    generate_method_body(&default_iface.name, &cast_expr, method, known_types)
}

fn generate_method_body(
    iface_var: &str,
    obj_expr: &str,
    method: &MethodMeta,
    known_types: &HashSet<String>,
) -> String {
    let in_params = get_in_params(method);
    let return_type = method.return_type.as_ref();
    let is_async = return_type.is_some_and(|rt| rt.is_async());
    // Methods with array out params (ReceiveArray/FillArray) return the array even without retval
    let has_array_out = method.params.iter().any(|p| {
        (p.direction == ParamDirection::Out || p.direction == ParamDirection::OutFill)
            && matches!(p.typ, TypeMeta::Array(_))
    });
    let has_return = return_type.is_some() || has_array_out;

    let mut out = String::new();

    // Event add/remove — TODO stubs
    if method.is_event_add {
        let event_name = to_camel_case(method.name.strip_prefix("add_").unwrap_or(&method.name));
        out.push_str(&format!("    // TODO: on{}(handler): event token\n", capitalize(&event_name)));
        return out;
    }
    if method.is_event_remove {
        let event_name = to_camel_case(method.name.strip_prefix("remove_").unwrap_or(&method.name));
        out.push_str(&format!("    // TODO: off{}(token): void\n", capitalize(&event_name)));
        return out;
    }

    if method.is_property_getter && in_params.is_empty() {
        // Property getter
        let prop_name = to_camel_case(method.name.strip_prefix("get_").unwrap_or(&method.name));
        let ts_return = ts_return_type_safe(return_type, false, known_types);
        out.push_str(&format!("    get {}(): {} {{\n", prop_name, ts_return));
        let invoke_expr = format!(
            "{}.method({}).invoke({}, [])",
            iface_var, method.vtable_index, obj_expr
        );
        let converted = convert_return(&invoke_expr, return_type, false);
        out.push_str(&format!("        return {};\n", converted));
        out.push_str("    }\n");
    } else if method.is_property_setter {
        // Property setter
        let prop_name = to_camel_case(method.name.strip_prefix("put_").unwrap_or(&method.name));
        let param_type = in_params.first()
            .map(|p| ts_param_type_safe(&p.typ, known_types))
            .unwrap_or_else(|| "any".to_string());
        out.push_str(&format!("    set {}(value: {}) {{\n", prop_name, param_type));
        let arg = in_params.first()
            .map(|p| wrap_arg("value", &p.typ))
            .unwrap_or_else(|| "value".to_string());
        out.push_str(&format!(
            "        {}.method({}).invoke({}, [{}]);\n",
            iface_var, method.vtable_index, obj_expr, arg
        ));
        out.push_str("    }\n");
    } else {
        // Regular method
        let ts_params = ts_param_list(&in_params, known_types);
        let ts_return = if has_array_out && return_type.is_none() {
            "DynWinRtValue".to_string()
        } else {
            ts_return_type_safe(return_type, is_async, known_types)
        };
        let method_name = to_camel_case(&method.name);
        let async_kw = if is_async { "async " } else { "" };

        out.push_str(&format!(
            "    {}{}({}): {} {{\n",
            async_kw, method_name, ts_params, ts_return
        ));

        let args_expr = build_args_expr(&in_params);
        let invoke_expr = format!(
            "{}.method({}).invoke({}, [{}])",
            iface_var, method.vtable_index, obj_expr, args_expr
        );

        if !has_return && !is_async {
            out.push_str(&format!("        {};\n", invoke_expr));
        } else {
            let converted = convert_return(&invoke_expr, return_type, is_async);
            out.push_str(&format!("        return {};\n", converted));
        }
        out.push_str("    }\n");
    }

    out
}

// ======================================================================
// Helper: argument wrapping
// ======================================================================

fn build_args_expr(in_params: &[&crate::meta::ParamMeta]) -> String {
    in_params.iter()
        .map(|p| wrap_arg(&to_camel_case(&p.name), &p.typ))
        .collect::<Vec<_>>()
        .join(", ")
}

fn wrap_arg(name: &str, typ: &TypeMeta) -> String {
    match typ {
        TypeMeta::String => format!("DynWinRtValue.hstring({})", name),
        TypeMeta::Bool => format!("DynWinRtValue.i32({} ? 1 : 0)", name),
        TypeMeta::I32 | TypeMeta::U32 | TypeMeta::Enum { .. }
        | TypeMeta::I8 | TypeMeta::U8 | TypeMeta::I16 | TypeMeta::U16
        | TypeMeta::Char16 => {
            format!("DynWinRtValue.i32({})", name)
        }
        TypeMeta::I64 | TypeMeta::U64 => format!("DynWinRtValue.i64({})", name),
        TypeMeta::RuntimeClass { .. } | TypeMeta::Object | TypeMeta::Interface { .. }
        | TypeMeta::Parameterized { .. } | TypeMeta::Delegate { .. } => {
            format!("({} as any)._obj ?? {}", name, name)
        }
        TypeMeta::Array(_) => format!("{}.toValue()", name),
        _ => name.to_string(),
    }
}

// ======================================================================
// Helper: return conversion
// ======================================================================

fn convert_return(expr: &str, return_type: Option<&TypeMeta>, is_async: bool) -> String {
    if is_async {
        // Unwrap async, then apply inner type conversion
        let inner_type = match return_type {
            Some(TypeMeta::AsyncOperation(inner)) => Some(inner.as_ref()),
            Some(TypeMeta::AsyncOperationWithProgress(inner, _)) => Some(inner.as_ref()),
            _ => None,
        };
        let awaited = format!("(await {}.toPromise())", expr);
        return convert_return(&awaited, inner_type, false);
    }
    match return_type {
        Some(TypeMeta::String) | Some(TypeMeta::Guid) => format!("{}.toString()", expr),
        Some(TypeMeta::I8 | TypeMeta::U8 | TypeMeta::I16 | TypeMeta::U16
            | TypeMeta::I32 | TypeMeta::U32 | TypeMeta::I64 | TypeMeta::U64
            | TypeMeta::F32 | TypeMeta::F64) => format!("{}.toNumber()", expr),
        Some(TypeMeta::Bool) => format!("{}.toNumber() !== 0", expr),
        Some(TypeMeta::Enum { .. }) => format!("{}.toNumber()", expr),
        // Wrap class/interface returns in their constructor
        Some(TypeMeta::RuntimeClass { name, .. }) => format!("new {}({})", name, expr),
        Some(TypeMeta::Interface { name, .. }) => format!("new {}({})", name, expr),
        Some(TypeMeta::Parameterized { name, args, .. }) => {
            let concrete = crate::meta::make_parameterized_name(name, args);
            format!("new {}({})", concrete, expr)
        }
        _ => expr.to_string(),
    }
}

