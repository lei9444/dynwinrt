/// Check if any type in the tree uses a known generic collection type.
pub fn uses_collections(classes: &[ClassMeta], interfaces: &[InterfaceMeta]) -> bool {
    fn check_type(typ: &TypeMeta) -> bool {
        match typ {
            TypeMeta::Parameterized { name, .. } => known_generic_name(name).is_some(),
            TypeMeta::AsyncOperation(inner) | TypeMeta::AsyncActionWithProgress(inner) => check_type(inner),
            TypeMeta::AsyncOperationWithProgress(r, p) => check_type(r) || check_type(p),
            TypeMeta::Array(inner) => check_type(inner),
            _ => false,
        }
    }
    fn check_methods(methods: &[MethodMeta]) -> bool {
        methods.iter().any(|m| {
            m.params.iter().any(|p| check_type(&p.typ))
                || m.return_type.as_ref().is_some_and(|rt| check_type(rt))
        })
    }
    for c in classes {
        if let Some(ref iface) = c.default_interface {
            if check_methods(&iface.methods) { return true; }
        }
        for iface in &c.factory_interfaces { if check_methods(&iface.methods) { return true; } }
        for iface in &c.static_interfaces { if check_methods(&iface.methods) { return true; } }
    }
    for i in interfaces {
        if check_methods(&i.methods) { return true; }
    }
    false
}

/// Generate an index.ts that re-exports all generated types.
pub fn generate_index(classes: &[ClassMeta], interfaces: &[InterfaceMeta], enums: &[TypeMeta]) -> String {
    let mut out = String::new();
    out.push_str("// Generated by winrt-meta — do not edit\n");
    for class in classes {
        out.push_str(&format!("export {{ {} }} from './{}';\n", class.name, class.name));
    }
    for iface in interfaces {
        out.push_str(&format!(
            "export {{ IID_{iname}, {iname} }} from './{iname}';\n",
            iname = iface.name
        ));
    }
    for en in enums {
        if let TypeMeta::Enum { name, .. } = en {
            out.push_str(&format!("export {{ {} }} from './{}';\n", name, name));
        }
    }
    out
}

/// Generate a TypeScript file for a single enum.
pub fn generate_enum(en: &TypeMeta) -> Option<String> {
    let (name, members) = match en {
        TypeMeta::Enum { name, members, .. } => (name, members),
        _ => return None,
    };

    let mut out = String::new();
    out.push_str("// Generated by winrt-meta — do not edit\n\n");
    out.push_str(&format!("export enum {} {{\n", name));
    for member in members {
        out.push_str(&format!("    {} = {},\n", member.name, member.value));
    }
    out.push_str("}\n");
    Some(out)
}

/// Generate a TypeScript file for a WinRT interface (non-exclusive).
/// Outputs IID, registerInterface, and a wrapper class with typed methods.
pub fn generate_interface(iface: &InterfaceMeta, known_types: &HashSet<String>) -> String {
    let mut out = String::new();
    out.push_str("// Generated by winrt-meta — do not edit\n");
    out.push_str("import { DynWinRtType, DynWinRtMethodSig, DynWinRtValue, DynWinRtArray, WinGuid } from 'dynwinrt-js';\n");
    let collection_names = collect_used_generics_from_methods(&iface.methods);
    for cname in &collection_names {
        if cname != &iface.name {
            out.push_str(&format!("import {{ {} }} from './{}';\n", cname, cname));
        }
    }

    // Type imports for referenced types (enums, classes, other interfaces)
    let type_imports = collect_iface_type_imports(iface);
    for (_ns, name, kind) in &type_imports {
        if known_types.contains(name) {
            out.push_str(&format_type_import(name, kind));
        }
    }
    out.push('\n');

    // IID constant
    if !iface.iid.is_empty() {
        out.push_str(&format!(
            "export const IID_{} = WinGuid.parse('{}');\n\n",
            iface.name, iface.iid
        ));
    }

    // Interface registration (private const, used by wrapper class)
    out.push_str(&format!("const _{} = DynWinRtType.registerInterface(\n", iface.name));
    out.push_str(&format!("    \"{}\", IID_{})\n", iface.name, iface.name));

    for method in &iface.methods {
        out.push_str(&format!(
            "    .addMethod(\"{}\", {})\n",
            method.name,
            build_method_sig(method)
        ));
    }
    trim_trailing_newline_add_semicolon(&mut out);
    out.push('\n');

    // Wrapper class
    out.push_str(&format!("export class {} {{\n", iface.name));
    out.push_str("    readonly _obj: DynWinRtValue;\n\n");
    out.push_str("    constructor(obj: DynWinRtValue) {\n");
    out.push_str("        this._obj = obj;\n");
    out.push_str("    }\n");

    // static from() — QI cast
    if !iface.iid.is_empty() {
        out.push('\n');
        out.push_str(&format!(
            "    static from(obj: DynWinRtValue): {} {{\n",
            iface.name
        ));
        out.push_str(&format!(
            "        return new {}(obj.cast(IID_{}));\n",
            iface.name, iface.name
        ));
        out.push_str("    }\n");
    }

    // Instance methods
    let iface_var = format!("_{}", iface.name);
    for method in &iface.methods {
        out.push('\n');
        out.push_str(&generate_iface_instance_method(iface, &iface_var, method, known_types));
    }

    out.push_str("}\n");
    out
}

/// Generate a TypeScript file for a single RuntimeClass.
/// Uses `registerInterface` + `invoke` pattern.
pub fn generate_class(class: &ClassMeta, known_types: &HashSet<String>) -> String {
    let mut out = String::new();

    // Header
    out.push_str("// Generated by winrt-meta — do not edit\n");
    out.push_str("import { DynWinRtType, DynWinRtMethodSig, DynWinRtValue, DynWinRtArray, WinGuid } from 'dynwinrt-js';\n");

    // Collection generics import
    let collection_names = collect_used_generics_from_class(class);
    for cname in &collection_names {
        out.push_str(&format!("import {{ {} }} from './{}';\n", cname, cname));
    }

    // Type imports — only for types that have generated .ts files
    let imports = collect_type_imports(class);
    for (_ns, name, kind) in &imports {
        if known_types.contains(name) {
            out.push_str(&format_type_import(name, kind));
        }
    }
    out.push('\n');

    // IID constants for all interfaces used by this class
    if let Some(ref iface) = class.default_interface {
        if !iface.iid.is_empty() {
            out.push_str(&format!(
                "const IID_{} = WinGuid.parse('{}');\n",
                iface.name, iface.iid
            ));
        }
    }
    for iface in &class.factory_interfaces {
        if !iface.iid.is_empty() {
            out.push_str(&format!(
                "const IID_{} = WinGuid.parse('{}');\n",
                iface.name, iface.iid
            ));
        }
    }
    for iface in &class.static_interfaces {
        if !iface.iid.is_empty() {
            out.push_str(&format!(
                "const IID_{} = WinGuid.parse('{}');\n",
                iface.name, iface.iid
            ));
        }
    }
    out.push('\n');

    // Interface registrations (exclusive interfaces used by this class)
    if let Some(ref iface) = class.default_interface {
        out.push_str(&generate_interface_registration(iface));
        out.push('\n');
    }
    for iface in &class.factory_interfaces {
        out.push_str(&generate_interface_registration(iface));
        out.push('\n');
    }
    for iface in &class.static_interfaces {
        out.push_str(&generate_interface_registration(iface));
        out.push('\n');
    }

    // Class declaration
    out.push_str(&format!("export class {} {{\n", class.name));
    out.push_str("    readonly _obj: DynWinRtValue;\n\n");
    out.push_str("    constructor(obj: DynWinRtValue) {\n");
    out.push_str("        this._obj = obj;\n");
    out.push_str("    }\n");

    // Default constructor
    if class.has_default_constructor {
        let has_create_factory = class.factory_interfaces.iter()
            .any(|iface| iface.methods.iter().any(|m| {
                let camel = to_camel_case(&m.name);
                camel == "create" || camel.starts_with("create")
            }));
        let ctor_name = if has_create_factory { "createDefault" } else { "create" };
        out.push('\n');
        out.push_str(&format!(
            "    static {}(): {} {{\n",
            ctor_name, class.name
        ));
        // activationFactory returns IActivationFactory; call ActivateInstance (vtable 6) to get instance
        out.push_str(&format!(
            "        return new {}(DynWinRtValue.activationFactory('{}').call(6, DynWinRtType.object(), [], []));\n",
            class.name, class.full_name
        ));
        out.push_str("    }\n");
    }

    // Factory methods
    for iface in &class.factory_interfaces {
        for method in &iface.methods {
            out.push('\n');
            out.push_str(&generate_factory_method_invoke(class, iface, method, known_types));
        }
    }

    // Static methods
    for iface in &class.static_interfaces {
        for method in &iface.methods {
            out.push('\n');
            out.push_str(&generate_static_method_invoke(class, iface, method, known_types));
        }
    }

    // Instance methods (from default interface)
    if let Some(ref default_iface) = class.default_interface {
        for method in &default_iface.methods {
            out.push('\n');
            out.push_str(&generate_instance_method_invoke(default_iface, method, known_types));
        }
    }

    out.push_str("}\n");
    out
}

// ======================================================================
// Interface registration helper
// ======================================================================

fn generate_interface_registration(iface: &InterfaceMeta) -> String {
    let mut out = String::new();
    out.push_str(&format!("const {} = DynWinRtType.registerInterface(\n", iface.name));
    out.push_str(&format!("    \"{}\", IID_{})\n", iface.name, iface.name));
    for method in &iface.methods {
        out.push_str(&format!(
            "    .addMethod(\"{}\", {})\n",
            method.name,
            build_method_sig(method)
        ));
    }
    trim_trailing_newline_add_semicolon(&mut out);
    out
}

fn trim_trailing_newline_add_semicolon(out: &mut String) {
    if out.ends_with(")\n") {
        out.truncate(out.len() - 1);
        out.push_str(";\n");
    }
}

// ======================================================================
// Method signature builder
// ======================================================================

/// Build a `new DynWinRtMethodSig().addIn(...)...addOut(...)` expression.
fn build_method_sig(method: &MethodMeta) -> String {
    let mut parts = Vec::new();

    // In params
    for param in &method.params {
        if param.direction == ParamDirection::In {
            parts.push(format!(".addIn({})", ts_dynwinrt_type(&param.typ)));
        }
    }

    // Out params (explicit [out] parameters in method signature)
    for param in &method.params {
        if param.direction == ParamDirection::Out {
            parts.push(format!(".addOut({})", ts_dynwinrt_type(&param.typ)));
        } else if param.direction == ParamDirection::OutFill {
            parts.push(format!(".addOutFill({})", ts_dynwinrt_type(&param.typ)));
        }
    }

    // Return type (WinRT return value = [out, retval])
    if let Some(ref return_type) = method.return_type {
        parts.push(format!(".addOut({})", ts_dynwinrt_type(return_type)));
    }

    if parts.is_empty() {
        "new DynWinRtMethodSig()".to_string()
    } else {
        format!("new DynWinRtMethodSig(){}", parts.join(""))
    }
}

