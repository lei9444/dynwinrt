// Well-known generic collection interface short names (from Windows.Foundation.Collections)
const KNOWN_GENERICS: &[(&str, &str, u8)] = &[
    ("IIterable`1", "IIterable", 1),
    ("IIterator`1", "IIterator", 1),
    ("IVectorView`1", "IVectorView", 1),
    ("IVector`1", "IVector", 1),
    ("IMapView`2", "IMapView", 2),
    ("IMap`2", "IMap", 2),
    ("IKeyValuePair`2", "IKeyValuePair", 2),
    ("IObservableVector`1", "IObservableVector", 1),
    ("IObservableMap`2", "IObservableMap", 2),
];

/// Map a Parameterized type name to its short generic TS name, if known.
fn known_generic_name(name: &str) -> Option<&'static str> {
    KNOWN_GENERICS.iter().find(|(n, _, _)| *n == name).map(|(_, short, _)| *short)
}

/// Format a Parameterized type as a TS generic type string, e.g. "IVectorView<string>".
fn ts_parameterized_type(name: &str, args: &[TypeMeta]) -> String {
    if let Some(short) = known_generic_name(name) {
        let ts_args: Vec<String> = args.iter().map(|a| ts_param_type(a)).collect();
        format!("{}<{}>", short, ts_args.join(", "))
    } else {
        "DynWinRtValue".to_string()
    }
}

